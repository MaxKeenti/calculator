console.log(substract(3, 6));
console.log(times(3, 6));
console.log(divide(3, 6));
console.log(percentage(10, 150));

function add(a, b) {
  return a + b;
}

function substract(a, b) {
  return a - b;
}

function times(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function percentage(a, b) {
  return (a * b) / 100;
}

